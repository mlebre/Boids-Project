agent::agent(void)
{
  speed=NULL;
  xposition=0;
  yposition=0;
  r=0;
  nb_agents=0;
}

agent::agent(double W, double H, unsigned int size)
{
  srandom(time(NULL));
  unsigned int i;
  speed=new double [4*size];
  for(i=0; i<4*size; i++)
  {
    speed[i]=0;
  }
  xposition=W*(random()/(RAND_MAX + 1.0));
  yposition=H*(random()/(RAND_MAX + 1.0));
  r=10;
  nb_agents=size;
}
