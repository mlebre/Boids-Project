unsigned int agent::nb_agents=0;


//-------------------------------------------------------------------
//                           Constructors
//-------------------------------------------------------------------
agent::agent(void)
{
  speed=0;
  xposition=0;
  yposition=0;
  r=0.5;
  nb_agents++;
}

agent::agent(double W, double H)
{
  srandom(time(NULL));
  speed=0;
  xposition=W*(random()/(RAND_MAX + 1.0));
  yposition=H*(random()/(RAND_MAX + 1.0));
  r=0.5;
  nb_agents++;
}
//-------------------------------------------------------------------
//                           Destructors
//-------------------------------------------------------------------
agent::~agent(void)
{
  nb_agents--;
}

